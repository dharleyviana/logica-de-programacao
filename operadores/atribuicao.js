// Só temos um operador de atríbuição: ( = ), mas ele pode ser usado em conjunto com vários outros operadores. Exemplos :

let x = 10;
x += 6;  // Aqui temos uma atribuição aditiviva/somar;
x -= 1; // Aqui temos uma atribuição de subtração;
console.log(x);

let y = 10; 
y = 4 // Aque temos uma substituição de valor, uma troca;
console.log(y);

// Essas atribuições também podem ser usadas com todos os operadores aritímeticos: ( +, -, *, /, %);


// Outro exemplo com strings;
let w= "texto";
w += ", mais texto";
console.log(w);

//OBS: atríbuição também pode significar que vai ser realizado uma: soma, concatenação, junção, agrupamento, adicionar, acrescentar, unir, agregar, anexar e etc. São todos sinônimos;